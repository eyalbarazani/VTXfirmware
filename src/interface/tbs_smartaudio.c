uint8_t crc8(const uint8_t *data, uint8_t data_length)
{
	uint8_t crc = 0;
	for (uint8_t i=0; i<data_length; i++) {
		crc = crc8table[crc ^ *data++];
	}
	return crc;
}



uint8_t host_to_vtx_cmd_unshift(uint8_t cmd)
{
	return (cmd >> 1);
}



uint8_t tbs_verify_crc8(uint8_t *raw_data)
{
	uint8_t data_length = raw_data[3];
	uint8_t msg_crc = raw_data[data_length + 4];

	if (crc8(raw_data, data_length + 4) != msg_crc) return 0;
	return 1;
}


uint8_t tbs_compose_frame(uint8_t data[], tbs_frame frame)
{
	data[0]= TBS_SYNC_BYTE;
	data[1] = TBS_HEADER_BYTE;
	data[2]= frame.command;
	data[3] = frame.data_length;
	
	for (uint8_t i = 0; i < frame.data_length; i++)
	{
		data[4 + i] = frame.payload[i];
	}

	data[4 + frame.data_length] = crc8(data, 4 + frame.data_length);

	return frame.data_length + 5;
}


