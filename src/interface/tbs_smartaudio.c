uint8_t tbs_host_msg_parsing(uint8_t *raw_data)
{
	tbs_frame host_msg;

	if (tbs_verify_crc8(raw_data) == 0) return 0;
	if ((raw_data[0] != TBS_SYNC_BYTE) || (raw_data[1] != TBS_HEADER_BYTE)) return 0;

	host_msg.command = host_to_vtx_cmd_unshift(raw_data[2]);
	host_msg.data_length = raw_data[3];

	for(uint8_t i = 0; i < host_msg.data_length; i++)
	{
		host_msg.payload[i] = raw_data[i + 4];
	}

	if (host_msg.command == TBS_GET_SETTINGS) 
		return tbs_get_settings(host_msg);
	if (host_msg.command == TBS_SET_POWER)
		return tbs_set_power(host_msg);
	if (host_msg.command == TBS_SET_CHANNEL) 
		return tbs_set_channel(host_msg);
	if (host_msg.command == TBS_SET_FREQUENCY) 
		return tbs_set_frequency(host_msg);
	if (host_msg.command == TBS_SET_OPERATION_MODE) 
		return tbs_set_operation_mode(host_msg);

	return 0;
}


